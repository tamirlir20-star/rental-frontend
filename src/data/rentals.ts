export type NeighborhoodData = {
  avgRent: number;
  medianRent: number;
  pricePerSqm: number;
  minRent: number;
  maxRent: number;
  yoyChange: number;
  sampleCount: number;
  trend: 'up' | 'down' | 'stable';
  history: { month: string; avg: number }[];
};

export type RoomType = 'studio' | '1' | '1.5' | '2' | '2.5' | '3' | '3.5' | '4' | '4+';

export type CityData = {
  lat: number;
  lng: number;
  nameHe: string;
  neighborhoods: Record<string, Record<RoomType, NeighborhoodData>>;
};

function makeHistory(baseAvg: number, trend: 'up' | 'down' | 'stable'): { month: string; avg: number }[] {
  const months = ['יוני 24', 'יולי 24', 'אוג 24', 'ספט 24', 'אוק 24', 'נוב 24', 'דצמ 24', 'ינו 25', 'פבר 25', 'מרץ 25', 'אפר 25', 'מאי 25'];
  const trendFactor = trend === 'up' ? 1.005 : trend === 'down' ? 0.997 : 1;
  let val = baseAvg * 0.92;
  return months.map(month => {
    val = Math.round(val * trendFactor + (Math.random() - 0.48) * 200);
    return { month, avg: val };
  });
}

function nd(avgRent: number, medianRent: number, pricePerSqm: number, yoyChange: number, sampleCount: number, trend: 'up' | 'down' | 'stable'): NeighborhoodData {
  return {
    avgRent,
    medianRent,
    pricePerSqm,
    minRent: Math.round(avgRent * 0.7),
    maxRent: Math.round(avgRent * 1.45),
    yoyChange,
    sampleCount,
    trend,
    history: makeHistory(avgRent, trend),
  };
}

